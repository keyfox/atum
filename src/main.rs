use rand::Rng;

const YES: &str = "\
###   ###  ########   #######     ###
###   ###  ###       ###   ###   ####
 ### ###   ###       ###         ###
  #####    ########   #######    ##
   ###     ###             ###   #
   ###     ###       ###   ###
   ###     ########   #######   ##
";
const NO: &str = "\
###   ###     #####       ##
####  ###   ###   ###    ####
##### ###  ###     ###   ###
### #####  ###     ###   ##
###  ####  ###     ###   #
###   ###   ###   ###
###   ###     #####     ##
";

fn main() {
    let mut r = rand::thread_rng();
    let result = match r.gen_range(0..=1) {
        0 => YES,
        1 => NO,
        _ => unreachable!()
    };
    println!("{}", result)
}
